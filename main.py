# Ввод числа от пользователя
user_input = int(input("Введите число: "))

# Переменная num принимает значение введенного пользователем числа
num = user_input

# Переменная is_prime устанавливается в True, пока не доказано обратное
is_prime = True

# Проверка, меньше ли число или равно 1 (такие числа не являются простыми)
if num <= 1:
    is_prime = False
else:
    # Инициализация переменной для цикла
    i = 2
    # Цикл выполняется, пока квадрат i не превысит значение num
    while i * i <= num:
        # Проверка, делится ли num на i без остатка
        if num % i == 0:
            # Если делится, число не является простым
            is_prime = False
            # Выход из цикла, так как число уже не может быть простым
            break
        # Увеличение i на единицу для проверки следующего делителя
        i += 1

# Проверка результата и вывод ответа пользователю
if is_prime:
    print(f"{user_input} - простое число")
else:
    print(f"{user_input} - не является простым числом")
